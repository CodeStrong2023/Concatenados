package tiposprimitivos.pkgchar;

public class TiposPrimitivosChar {

    public static void main(String[] args) {
        char myVariableChar = 'a';
        System.out.println("myVariableChar = " + myVariableChar);
        
        char varCaracter = '\u0024';
        System.out.println("varCaracter = " + varCaracter);
        
        char varCaracterDecimal = 36;
        System.out.println("varCaracterDecimal = " + varCaracterDecimal);
        
        char varCaracterSimbolo = '$';
        System.out.println("varCaracterSimbolo = " + varCaracterSimbolo);
        
        
        var varCaracter1 = '\u0024';
        System.out.println("varCaracter = " + varCaracter);
        
        var varCaracterDecimal1 = (char)36;
        System.out.println("varCaracterDecimal = " + varCaracterDecimal);
        
        var varCaracterSimbolo1 = '$';
        System.out.println("varCaracterSimbolo = " + varCaracterSimbolo);
        
        int varEnteroChar = '$';
        System.out.println("varEnteroChar = " + varEnteroChar);
        int charCaracter = 'Z';
        System.out.println("charCaracter = " + charCaracter);
    }
    
}
